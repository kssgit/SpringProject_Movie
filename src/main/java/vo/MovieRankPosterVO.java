package vo;

public class MovieRankPosterVO {
	private int idx;
	private String movieNm;
	private String posterNm;
	private String trailerSrc;
	
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public String getMovieNm() {
		return movieNm;
	}
	public void setMovieNm(String movieNm) {
		this.movieNm = movieNm;
	}
	public String getPosterNm() {
		return posterNm;
	}
	public void setPosterNm(String posterNm) {
		this.posterNm = posterNm;
	}
	public String getTrailerSrc() {
		return trailerSrc;
	}
	public void setTrailerSrc(String trailerSrc) {
		this.trailerSrc = trailerSrc;
	}
	
}
